// variables in JS
// there are three types to declare variables in JS

//  1 --> var
var A = 202; // this is can be redeclared and reassigned 

//  2 --> let
let B = 303; // this can be reassigned but we cannot redeclared it 

//  3 --> const 
const C = 404; // not reassined and not redeclare (constant) 

/*
const is used to declare constants in code also const is block scope 

let is global scoped
var is local scoped 
here block scope = local scope 
*/
